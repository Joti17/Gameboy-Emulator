COLOR = {
    0: "E",
    1: "#",
    2: "*",
    3: "\\"
}

def decode(sprite: bytearray):
    for i in range(8):
        low_bits = sprite[i*2]
        high_bits = sprite[i*2 + 1]
        for j in range(8):
            bit = 7 - j
            color = int(((high_bits >> (bit) & 1) << 1) | ((low_bits >> bit) & 1))
            print(COLOR[color], end=" ")
        print("")

